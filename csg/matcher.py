def _object_fact_features(g: CanonGraph, articulation_tol: float) -> Dict[str, List[Tuple[Tuple, Optional[str]]]]:
    """Per relevant object, the time-free structural facts it participates in,
    as (label, neighbor_object_or_None) pairs. Planner goals are deliberately
    excluded: they exist only on the target side and depend on no observation,
    so including them would make target/robot fingerprints incomparable and
    break time-warp invariance of the alignment."""
    feats: Dict[str, List[Tuple[Tuple, Optional[str]]]] = defaultdict(list)
    for obs in _relation_timeline(g).values():
        for (s, o, rel) in {(s, o, rel) for (_, s, o, rel) in obs}:
            if rel in SYMMETRIC_RELATIONS:
                feats[s].append((("R", rel, "sym"), o))
                feats[o].append((("R", rel, "sym"), s))
            else:
                feats[s].append((("R", rel, "subj"), o))
                feats[o].append((("R", rel, "obj"), s))
    for e in g.events:
        for oid in e.objects:
            others = [x for x in e.objects if x != oid]
            if others:
                feats[oid].extend(((("E", e.kind), other) for other in others))
            else:
                feats[oid].append((("E", e.kind), None))
    for c in g.contact_facts:
        for me, other in ((c.a, c.b), (c.b, c.a)):
            if me not in g.objects:
                continue
            if other in g.objects:
                feats[me].append((("C", c.mode), other))
            else:
                feats[me].append((("C", c.mode, other), None))
    arts = _articulation_endpoints(g, {})
    for oid, (first, last, jk, vk) in arts.items():
        direction = "FLAT"
        if first is not None and last is not None:
            if last - first > articulation_tol:
                direction = "INCREASE"
            elif first - last > articulation_tol:
                direction = "DECREASE"
        feats[oid].append((("A", jk, vk, direction), None))
    return feats


def _role_fingerprints(g: CanonGraph, articulation_tol: float, rounds: int = 2) -> Dict[str, Tuple]:
    """1-WL color refinement over the structural fact graph. Initial color is
    the hard carrier signature; each round folds in the sorted multiset of
    (fact label, neighbor color). Colors are pure structural tuples (no object
    ids), so fingerprints are directly comparable across graphs. Objects with
    equal fingerprints within one graph are role-interchangeable (an orbit)."""
    feats = _object_fact_features(g, articulation_tol)
    objs = list(g.relevant_objects)
    color: Dict[str, Tuple] = {
        o: (g.objects[o].hard_signature() if o in g.objects else ("EXT",)) for o in objs
    }

    def col(o: Optional[str]) -> Optional[Tuple]:
        if o is None:
            return None
        return color.get(o, ("EXT",))

    for _ in range(max(0, rounds)):
        color = {
            o: (color[o], tuple(sorted(((lab, col(nb)) for (lab, nb) in feats.get(o, [])), key=repr)))
            for o in objs
        }
    return color


def _feature_labels(feats: Mapping[str, List[Tuple[Tuple, Optional[str]]]], oid: str) -> Dict[Tuple, int]:
    out: Dict[Tuple, int] = defaultdict(int)
    for (lab, _) in feats.get(oid, []):
        out[lab] += 1
    return dict(out)


def _label_similarity(a: Mapping[Tuple, int], b: Mapping[Tuple, int]) -> float:
    """Multiset Jaccard over fact labels (neighbor-free), robust to the robot
    rollout being a fragmentation-refined superset of the demonstration."""
    if not a and not b:
        return 1.0
    inter = sum(min(a.get(k, 0), b.get(k, 0)) for k in set(a) | set(b))
    union = sum(max(a.get(k, 0), b.get(k, 0)) for k in set(a) | set(b))
    return inter / union if union else 1.0


def _candidate_mappings(target: CanonGraph, robot: CanonGraph, cfg: MatcherConfig) -> List[Dict[str, str]]:
    """Candidate bijections from target relevant objects to robot relevant
    objects, generated by a similarity-guided DFS over role fingerprints
    instead of N! enumeration (audit A6).

    Target objects are assigned most-constrained-first (descending fact
    degree); robot candidates must share the hard carrier signature and are
    tried in descending fact-label similarity. Equal-fingerprint objects are
    interchangeable, so the first mappings the DFS yields are the highest-
    likelihood role alignments; the search is capped at
    ``cfg.max_candidate_mappings`` complete mappings. A target object with no
    free same-signature robot object maps to itself, which the carrier probe
    then flags (same semantics as the old greedy fallback)."""
    A = target.relevant_objects
    B = robot.relevant_objects
    if not A:
        return [{}]

    t_feats = _object_fact_features(target, cfg.articulation_tol)
    r_feats = _object_fact_features(robot, cfg.articulation_tol)
    t_labels = {ao: _feature_labels(t_feats, ao) for ao in A}
    r_labels = {bo: _feature_labels(r_feats, bo) for bo in B}
    t_fp = _role_fingerprints(target, cfg.articulation_tol, cfg.wl_rounds)
    r_fp = _role_fingerprints(robot, cfg.articulation_tol, cfg.wl_rounds)

    # Most-constrained target objects first: their assignment prunes hardest.
    order = sorted(A, key=lambda o: (-sum(t_labels[o].values()), o))
    ranked: Dict[str, List[str]] = {}
    for ao in order:
        sig = target.objects[ao].hard_signature() if ao in target.objects else ("EXT",)
        cands = [bo for bo in B if (robot.objects[bo].hard_signature() if bo in robot.objects else ("EXT",)) == sig]
        ranked[ao] = sorted(
            cands,
            key=lambda bo: (
                0 if t_fp.get(ao) == r_fp.get(bo) else 1,  # exact role match first
                -_label_similarity(t_labels[ao], r_labels[bo]),
                bo,
            ),
        )

    results: List[Dict[str, str]] = []
    used: set = set()
    cur: Dict[str, str] = {}

    def dfs(i: int) -> None:
        if len(results) >= cfg.max_candidate_mappings:
            return
        if i == len(order):
            results.append(dict(cur))
            return
        ao = order[i]
        cands = [bo for bo in ranked[ao] if bo not in used]
        if not cands:
            # No free same-signature robot object: self-map so carrier flags it.
            cur[ao] = ao
            dfs(i + 1)
            del cur[ao]
            return
        for bo in cands:
            used.add(bo)
            cur[ao] = bo
            dfs(i + 1)
            used.discard(bo)
            del cur[ao]
            if len(results) >= cfg.max_candidate_mappings:
                return

    dfs(0)
    if not results:
        results.append({ao: ao for ao in A})
    return results


def _fingerprint_orbits(fp: Mapping[str, Tuple]) -> List[List[str]]:
    """Equal-fingerprint classes with more than one member (true symmetry
    orbits: their members are role-interchangeable in that graph)."""
    by_color: Dict[Tuple, List[str]] = defaultdict(list)
    for oid, color in fp.items():
        by_color[color].append(oid)
    return sorted([sorted(v) for v in by_color.values() if len(v) > 1])


def match(target_raw: Json, robot_raw: Json, cfg: Optional[MatcherConfig] = None) -> MatchResult:
    cfg = cfg or MatcherConfig()
    target = canonical_form(target_raw, role="target", cfg=cfg.canon)
    robot = canonical_form(robot_raw, role="rollout", cfg=cfg.canon)

    hard = list(HARD_PROBES)
    if target.requires_contact_word:
        hard.append("contact_word")
    hard_probes = tuple(hard)

    candidates = _candidate_mappings(target, robot, cfg)
    best: Optional[Tuple[int, float, Dict[str, str], Dict[str, float], Dict[str, bool], Dict[str, int]]] = None
    keys: List[Tuple[int, float]] = []
    for mapping in candidates:
        distances, agreement, support = _evaluate_mapping(target, robot, mapping, cfg, hard_probes)
        n_hard_agree = sum(1 for p in hard_probes if agreement[p])
        soft_d = sum(cfg.weights[k] * distances[k] for k in distances)
        keys.append((n_hard_agree, soft_d))
        key = (n_hard_agree, -soft_d)
        if best is None or key > (best[0], -best[1]):
            best = (n_hard_agree, soft_d, mapping, distances, agreement, support)

    assert best is not None
    best_agree, best_soft, mapping, distances, agreement, support = best
    # Orbit ambiguity: the best mapping is not unique. Either several evaluated
    # candidates tie on the full (hard-agreement, soft-distance) key, or the
    # target itself contains a symmetry orbit (equal role fingerprints), in
    # which case the unexplored permutations of that orbit are equivalent too.
    tied = sum(1 for (n, d) in keys if n == best_agree and abs(d - best_soft) <= cfg.same_class_eps)
    target_orbits = _fingerprint_orbits(_role_fingerprints(target, cfg.articulation_tol, cfg.wl_rounds))
    relevant_orbits = [orb for orb in target_orbits if any(o in mapping for o in orb)]
    orbit_ambiguous = (tied > 1 and len(candidates) > 1) or bool(relevant_orbits)

    # Vacuity gate (audit A1/A2): a target that constrains nothing
    # task-defining must not accept every rollout. Probe agreement with zero
    # support is agreement about nothing.
    vacuous = all(support.get(p, 0) == 0 for p in cfg.task_defining_probes)
    passed = all(agreement[p] for p in hard_probes) and not (vacuous and cfg.require_support)

    # Estimator-dependent object attributes excluded from the hard carrier
    # signature, surfaced as soft diagnostics only.
    soft_profile_mismatches = {}
    for t_oid, r_oid in mapping.items():
        tp, rp = target.objects.get(t_oid), robot.objects.get(r_oid)
        if tp and rp and (tp.geometry_kind, tp.parts) != (rp.geometry_kind, rp.parts):
            soft_profile_mismatches[f"{t_oid}->{r_oid}"] = {
                "target": [tp.geometry_kind, list(tp.parts)],
                "robot": [rp.geometry_kind, list(rp.parts)],
            }
    den = sum(cfg.weights.values())
    distance = sum(cfg.weights[k] * distances[k] for k in distances) / max(den, 1e-12)
    if distance < 1e-12:
        distance = 0.0

    return MatchResult(
        passed=passed,
        distance=distance,
        probe_agreement=agreement,
        probe_distance=distances,
        hard_probes=hard_probes,
        object_mapping=mapping,
        object_orbit_ambiguous=orbit_ambiguous,
        probe_support=support,
        vacuous=vacuous,
        diagnostics={
            "target_relevant_objects": target.relevant_objects,
            "robot_relevant_objects": robot.relevant_objects,
            "robot_observed_channels": robot.observed_channels,
            "robot_leakage": robot.leakage,
            "requires_contact_word": target.requires_contact_word,
            "contact_word_pairs": sorted(map(list, target.contact_word_pairs)),
            "n_candidate_mappings": len(candidates),
            "n_best_tied_mappings": tied,
            "target_symmetry_orbits": relevant_orbits,
            "object_profile_soft_mismatches": soft_profile_mismatches,
        },
    )


# Backwards-compatible aliases.
def match_csg_json(human_json: Json, robot_json: Json, cfg: Optional[MatcherConfig] = None) -> MatchResult:
    return match(human_json, robot_json, cfg)


def match_csg_files(human_path: str, robot_path: str, cfg: Optional[MatcherConfig] = None) -> MatchResult:
    return match(load_json(human_path), load_json(robot_path), cfg)


def _parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="CSG probe-based quotient checker.")
    p.add_argument("target_json")
    p.add_argument("robot_json")
    p.add_argument("--json", action="store_true")
    return p.parse_args()


def main() -> None:
    args = _parse_args()
    result = match_csg_files(args.target_json, args.robot_json)
    if args.json:
        print(json.dumps(result.to_json(), indent=2, sort_keys=True))
    else:
        print(f"passed={result.passed}")
        print(f"vacuous={result.vacuous}")
        print(f"distance={result.distance:.6f}")
        print(f"object_mapping={result.object_mapping}")
        print("hard probes:")
        for p in result.hard_probes:
            print(f"  {p}: {'OK' if result.probe_agreement[p] else 'MISMATCH'} ({result.probe_distance[p]:.4f}, support={result.probe_support.get(p, 0)})")
        soft = [p for p in SOFT_PROBES if p not in result.hard_probes]
        print("soft probes:")
        for p in soft:
            print(f"  {p}: {result.probe_distance[p]:.4f}")


if __name__ == "__main__":
    main()
